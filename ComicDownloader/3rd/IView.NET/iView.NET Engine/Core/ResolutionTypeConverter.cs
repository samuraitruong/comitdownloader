namespace IView.Engine.Core
{
    /// <summary>
    /// Provides a type converter for the Resolution structure.
    /// </summary>
    public class ResolutionTypeConverter : ExpandableObjectConverter
    {
        #region Methods

        /// <summary>
        /// 
        /// </summary>
        /// <param name="context"></param>
        /// <param name="sourceType"></param>
        /// <returns></returns>
        public override bool CanConvertFrom(ITypeDescriptorContext context, System.Type sourceType)
        {
            if (sourceType == typeof(string))
                return true;

            return base.CanConvertFrom(context, sourceType);
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="context"></param>
        /// <param name="destinationType"></param>
        /// <returns></returns>
        public override bool CanConvertTo(ITypeDescriptorContext context, System.Type destinationType)
        {
            if (destinationType == typeof(Resolution))
                return true;

            return base.CanConvertTo(context, destinationType);
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="context"></param>
        /// <param name="culture"></param>
        /// <param name="value"></param>
        /// <param name="destinationType"></param>
        /// <returns></returns>
        public override object ConvertTo(ITypeDescriptorContext context, System.Globalization.CultureInfo culture, object value, System.Type destinationType)
        {
            if ((destinationType == typeof(string)) && value is Resolution)
            {
                Resolution Res = (Resolution)value;
                return Res.Horizontal + ", " + Res.Vertical;
            }

            return base.ConvertTo(context, culture, value, destinationType);
        }

        #endregion
    }
}
